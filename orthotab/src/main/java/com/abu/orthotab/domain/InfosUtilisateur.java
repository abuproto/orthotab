public class InfosUtilisateur {
	private String libUtilisateur;

	private String paramInterjour;

	private Long nbTotCac;
	
	private String libNivCourant;

	public String getLibUtilisateur() {
		return libUtilisateur;
	}

	public void setLibUtilisateur(String libUtilisateur) {
		this.libUtilisateur = libUtilisateur;
	}

	public String getParamInterjour() {
		return paramInterjour;
	}

	public void setParamInterjour(String paramInterjour) {
		this.paramInterjour = paramInterjour;
	}

	public Long getNbTotCac() {
		return nbTotCac;
	}

	public void setNbTotCac(Long nbTotCac) {
		this.nbTotCac = nbTotCac;
	}

	public String getLibNivCourant() {
		return libNivCourant;
	}

	public void setLibNivCourant(String libNivCourant) {
		this.libNivCourant = libNivCourant;
	}

}
