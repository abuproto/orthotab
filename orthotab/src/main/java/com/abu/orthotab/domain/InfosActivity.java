package com.abu.orthotab.domain;

public class InfosActivity {
	private String libUtilisateur;
	
	private String noSemaine;
	
	private String exercice;
	
	private String duree;
	
	private String nbEchec;
	
	private String dateIa;

	public String getLibUtilisateur() {
		return libUtilisateur;
	}

	public void setLibUtilisateur(String libUtilisateur) {
		this.libUtilisateur = libUtilisateur;
	}

	public String getNoSemaine() {
		return noSemaine;
	}

	public void setNoSemaine(String noSemaine) {
		this.noSemaine = noSemaine;
	}

	public String getExercice() {
		return exercice;
	}

	public void setExercice(String exercice) {
		this.exercice = exercice;
	}

	public String getDuree() {
		return duree;
	}

	public void setDuree(String duree) {
		this.duree = duree;
	}

	public String getNbEchec() {
		return nbEchec;
	}

	public void setNbEchec(String nbEchec) {
		this.nbEchec = nbEchec;
	}

	public String getDateIa() {
		return dateIa;
	}

	public void setDateIa(String dateIa) {
		this.dateIa = dateIa;
	}
	
	
}
